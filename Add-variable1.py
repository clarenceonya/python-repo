# store input numbers from the user
a = input("enter the first number: ")
b = input("enter the second number: ")

# add the numbers
sum = float(a)+float(b)

# print results
print("the sum of {0} and {1} is {2}".format(a,b,sum))

# multiply the numbers 
multiply = float(a)*float(b)

# print results
print("the multiplication of {0} and {1} is {2}".format(a,b,multiply))

# divide the numbers
divide = float(a)/float(b)

# print results
print("the division of {0} and {1} is {2}".format(a,b,divide))

# subtract the numbers
subtract = float(a)-float(b)

# print results
print("the subtraction of {0} and {1} is {2}".format(a,b,subtract))